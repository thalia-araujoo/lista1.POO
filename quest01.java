class ImprimirNumeros {
    
    public static void main(String[] args) {
        // Início do intervalo
        int inicio = 150;
        
        // Fim do intervalo
        int fim = 300;
        
        // Loop para imprimir os números de 150 a 300
        for (int i = inicio; i <= fim; i++) {
            System.out.println(i);
        }
    }
}
